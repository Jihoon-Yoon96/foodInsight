const generateData = () => {
    const allDummyData = [];
    let reportNoBase = 20240001; // 고유 신고번호 베이스

    for (const [type, words] of Object.entries(config)) {
        let count = 0;
        // 3가지 배열을 조합하여 품목당 정확히 100개의 고유한 데이터를 생성합니다.
        for (const a of words.A) {
            for (const b of words.B) {
                for (const c of words.C) {
                    if (count >= 100) break; // 딱 100개까지만 생성

                    const productName = `${a} ${b} ${c}`;
                    const factory = factories[count % factories.length];
                    const rawMaterial = rawMaterialsList[count % rawMaterialsList.length];
                    // 그럴듯한 최근 보고일자 생성 (예: 20240514)
                    const month = String((count % 12) + 1).padStart(2, '0');
                    const day = String((count % 28) + 1).padStart(2, '0');

                    allDummyData.push({
                        PRDLST_REPORT_NO: `FACT-${reportNoBase++}`, // 현실적인 신고번호
                        PRDLST_NM: productName,
                        BSSH_NM: factory,
                        PRDLST_DCNM: type,
                        RAWMTRL_NM: rawMaterial,
                        PRMS_DT: `2024${month}${day}`
                    });
                    count++;
                }
            }
        }
    }
    return allDummyData; // 총 1000개의 완성된 데이터 반환
};

